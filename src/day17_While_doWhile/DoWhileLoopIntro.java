package day17_While_doWhile;

public class DoWhileLoopIntro {
    public static void main(String[] args) {
        boolean a = false;


        while (a){
            System.out.println("Wooden Spoon");


        }

        System.out.println("________________________________");

        do{
            System.out.println("Wooden Spoon");
        }while (a);



    }
}
